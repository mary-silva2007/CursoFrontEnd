//Funções Matemáticas 

//SQRT e POW (Quadrado e Potência)

//Calcular a raiz quadrada de 25
console.log(Math.sqrt(25)); //5

//calcular a potência de nº pelo outro 
console.log(Math.pow(7,2)); //7² = 49
console.log(Math.pow(4,3)); //4³ = 64
console.log(Math.pow(27,1/3)) //³v27 = 3

//funções de arredondamento (round,. ceil, floor)
console.log(Math.round(4.7)); //5
console.log(Math.round(4.4)); //4
console.log(Math.ceil(4.1)); //5 
console.log(Math.floor(4.9)); //4 

//Números Aleatórios (Math.ramdon)
console.log(Math.random()); // gerar um nº entre (0,1)
console.log(Math.random()*1000)// gera um número enre (0 e 999,99)

//Valor Absoluto
console.log(Math.abs(-10)); //10

//Minimos e Máximos 
console.log(Math.min(1,2,3,4,5,6,7,8,9)); //1
console.log(Math.max(1,2,3,4,5,6,7,8,9)); //9
